"""
Защита от Unicode homoglyph атак.

Атакующий может написать сurl (кириллическая с, U+0441) вместо curl —
визуально неотличимо, но regex не совпадёт. Решение: заменить все
визуально похожие символы на их ASCII-эквиваленты ДО анализа.

Покрывает:
  - Кириллические буквы визуально похожие на латиницу (с→c, о→o, р→r…)
  - Fullwidth латиница (ｃ→c, ｕ→u, ｒ→r…)
  - Zero-width символы (убираются, чтобы c​url → curl)
  - Soft hyphen (U+00AD — невидимый, используется для разрыва слов)
"""

_TABLE: dict[str, str] = {
    # ── Кириллица → латиница ──────────────────────────────────────────
    'а': 'a', 'е': 'e', 'о': 'o', 'р': 'r', 'с': 'c',
    'у': 'y', 'х': 'x', 'і': 'i', 'ј': 'j',
    'А': 'A', 'В': 'B', 'Е': 'E', 'К': 'K', 'М': 'M',
    'Н': 'H', 'О': 'O', 'Р': 'P', 'С': 'C', 'Т': 'T',
    'У': 'Y', 'Х': 'X', 'І': 'I',
    # ── Fullwidth латиница (U+FF41..FF5A, U+FF21..FF3A) ───────────────
    '\uff41': 'a', '\uff42': 'b', '\uff43': 'c', '\uff44': 'd',
    '\uff45': 'e', '\uff46': 'f', '\uff47': 'g', '\uff48': 'h',
    '\uff49': 'i', '\uff4a': 'j', '\uff4b': 'k', '\uff4c': 'l',
    '\uff4d': 'm', '\uff4e': 'n', '\uff4f': 'o', '\uff50': 'p',
    '\uff51': 'q', '\uff52': 'r', '\uff53': 's', '\uff54': 't',
    '\uff55': 'u', '\uff56': 'v', '\uff57': 'w', '\uff58': 'x',
    '\uff59': 'y', '\uff5a': 'z',
    '\uff21': 'A', '\uff22': 'B', '\uff23': 'C', '\uff24': 'D',
    '\uff25': 'E', '\uff26': 'F', '\uff27': 'G', '\uff28': 'H',
    '\uff29': 'I', '\uff2a': 'J', '\uff2b': 'K', '\uff2c': 'L',
    '\uff2d': 'M', '\uff2e': 'N', '\uff2f': 'O', '\uff30': 'P',
    '\uff31': 'Q', '\uff32': 'R', '\uff33': 'S', '\uff34': 'T',
    '\uff35': 'U', '\uff36': 'V', '\uff37': 'W', '\uff38': 'X',
    '\uff39': 'Y', '\uff3a': 'Z',
    # ── Zero-width / невидимые символы → удалить ─────────────────────
    '\u200b': '',   # zero-width space
    '\u200c': '',   # zero-width non-joiner
    '\u200d': '',   # zero-width joiner
    '\ufeff': '',   # BOM / zero-width no-break space
    '\u00ad': '',   # soft hyphen (невидим, разрывает слова)
    '\u2060': '',   # word joiner
    '\u180e': '',   # mongolian vowel separator
}

_TRANS = str.maketrans(_TABLE)


def sanitize(text: str) -> str:
    """Возвращает текст с заменёнными homoglyph-символами.

    Исходный текст не изменяется — возвращается новая строка.
    Вызывается один раз в начале analyze() перед всеми проверками.
    """
    return text.translate(_TRANS)
